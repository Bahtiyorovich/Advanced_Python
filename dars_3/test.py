
def daraja(a, b, c, d):
    """ a ning b va c darajalarini aniqlab ko'rsatuvchi funksiya """
    pow_a = a ** b
    pow_c = c ** d
    return f"{a} ning {b} darajasi {pow_a}, {c} ning {d} darajasi {pow_c}"

